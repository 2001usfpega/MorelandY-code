package com.revature.javacore;

public class QuestionTen {
	//Ternary Operator

	public static void main(String[] args) {
		
	
		
		int taco = 10;
		int nacho = 15;
	// a < b ? a : b to check for the smallest number
		
		int minVal = (taco < nacho ? taco : nacho);
		System.out.println(minVal);{
	// because 10 is less that 15, it is the minimum value and is printed to the console.
		
	}
	}

	
	}


